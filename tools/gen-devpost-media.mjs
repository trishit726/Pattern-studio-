// Generate DEVPOST_MEDIA.md — ready-to-paste jsDelivr GIF embeds for Devpost.
//
// Derives owner/repo from `git remote get-url origin` at runtime, so if the
// repository is renamed or moved to another account, just re-run this and every
// URL updates automatically:
//
//   node tools/gen-devpost-media.mjs                 # default ref = "devpost-media" tag
//   node tools/gen-devpost-media.mjs --ref main      # pin to a branch
//   node tools/gen-devpost-media.mjs --ref <sha>     # pin to an immutable commit
//
// Why a TAG (devpost-media) by default instead of @main:
//   * jsDelivr @<ref> cannot contain "/", so a slash branch like
//     "feat/templates-studio-dynamodb" breaks. A tag is slash-free and stable.
//   * A tag is immutable on the CDN, so a pasted Devpost link never breaks.
import { execSync } from "node:child_process"
import fs from "node:fs"
import path from "node:path"

const ROOT = process.cwd()
const ASSETS_DIR = path.join(ROOT, "assets")
const WIDTHS = [600, 800, 900, 1000] // 900 = the size in the Devpost example
const DEFAULT_REF = "devpost-media"

const argRef = (() => {
  const i = process.argv.indexOf("--ref")
  return i >= 0 ? process.argv[i + 1] : null
})()
const REF = argRef || process.env.DEVPOST_REF || DEFAULT_REF

// Parse owner/repo from either https or ssh GitHub remotes.
function ownerRepo() {
  const url = execSync("git remote get-url origin", { encoding: "utf8" }).trim()
  const m = url.match(/github\.com[:/]+([^/]+)\/(.+?)(?:\.git)?$/i)
  if (!m) throw new Error(`Could not parse GitHub owner/repo from remote: ${url}`)
  return { owner: m[1], repo: m[2] }
}

const { owner, repo } = ownerRepo()
const base = `https://cdn.jsdelivr.net/gh/${owner}/${repo}@${REF}/assets`

if (!fs.existsSync(ASSETS_DIR)) throw new Error("assets/ folder not found — create it and add your GIFs first.")
const gifs = fs.readdirSync(ASSETS_DIR).filter((f) => f.toLowerCase().endsWith(".gif")).sort()
if (!gifs.length) throw new Error("No .gif files found in assets/.")

const titleFor = (f) =>
  f.replace(/\.gif$/i, "").replace(/^demo[-_]/i, "").replace(/[-_]+/g, " ").replace(/\b\w/g, (c) => c.toUpperCase())
const urlFor = (f) => `${base}/${encodeURIComponent(f)}`

let md = ""
md += `# Devpost Media — ready-to-paste GIF embeds\n\n`
md += `Auto-generated by \`tools/gen-devpost-media.mjs\`. **Copy a snippet below and paste it straight into your Devpost project story** — the GIF animates inline.\n\n`
md += `| | |\n|---|---|\n`
md += `| **Repo** | \`${owner}/${repo}\` |\n`
md += `| **CDN** | jsDelivr (\`cdn.jsdelivr.net/gh/...\`) |\n`
md += `| **Ref** | \`${REF}\` |\n`
md += `| **GIFs** | ${gifs.length} |\n\n`
md += `> Renamed the repo or account, or added a GIF? Re-run:\n> \`\`\`bash\n> node tools/gen-devpost-media.mjs --ref ${REF}\n> \`\`\`\n\n`
md += `---\n\n`

for (const f of gifs) {
  const alt = `Pattern Studio — ${titleFor(f)}`
  const url = urlFor(f)
  md += `## ${titleFor(f)}\n\n`
  md += `\`assets/${f}\` → ${url}\n\n`
  md += `**Devpost HTML** (centered — pick a width):\n\n`
  for (const w of WIDTHS) {
    md += "```html\n"
    md += `<p align="center">\n  <img src="${url}" width="${w}" alt="${alt}">\n</p>\n`
    md += "```\n\n"
  }
  md += `**GitHub README Markdown:**\n\n`
  md += "```markdown\n"
  md += `![${alt}](${url})\n`
  md += "```\n\n"
  md += `---\n\n`
}

fs.writeFileSync(path.join(ROOT, "DEVPOST_MEDIA.md"), md)
console.log(`Wrote DEVPOST_MEDIA.md — ${gifs.length} GIF(s) @ ${REF}`)
gifs.forEach((f) => console.log("  " + urlFor(f)))
